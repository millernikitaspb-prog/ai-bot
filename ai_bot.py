import telebot 
from groq import Groq 
from flask import Flask, request
import os

BOT_TOKEN = os.environ.get("BOT_TOKEN")
GROQ_API_KEY = os.environ.get("GROQ_API_KEY")

bot = telebot.TeleBot(BOT_TOKEN)
client = Groq(api_key=GROQ_API_KEY)
app = Flask(__name__)

histories = {}

@bot.message_handler(commands=['start'])
def start(message):
	histories[message.chat.id] = []
	bot.send_message(message.chat.id, "Привет! Я AI бот. Задай мне любой вопрос 🤖")

@bot.message_handler(commands=['clear'])
def clear(message):
	histories[message.chat.id] = []
	bot.send_message(message.chat.id, "История очищена! Начинаем заново 🔄")

@bot.message_handler(func=lambda message: True)
def ask_ai(message):
	chat_id = message.chat.id 

	if chat_id not in histories:
		histories[chat_id] = []

	histories[chat_id].append({
		"role": "user",
		"content": message.text
	})

	bot.send_message(chat_id, "Думаю... ⏳")

	response = client.chat.completions.create(
		model="llama-3.3-70b-versatile",
		messages=[
			{"role": "system", "content": """Ты — философский собеседник. Твоя задача — помочь пользователю глубже понять его взгляды через историю философии.
Структура ответа (максимум 200 слов)
 1. Покажи, что понял мысль пользователя — переформулируй её суть одним предложением, не повторяя слово в слово.
 2. Назови 1–2 философов, чьи идеи перекликаются с позицией пользователя. Укажи конкретную работу и кратко передай аргумент.
 3. Приведи контраргумент от 1–2 других философов — тоже с работой и сутью возражения.
 4. Заверши одним открытым вопросом, который подталкивает думать дальше.
Стиль и тон
 ∙ Пиши как начитанный друг за кофе: тепло, на «ты», без снобизма и менторства.
 ∙ Веди диалог, а не читай лекцию. Думай вместе с собеседником, а не над ним.
 ∙ Будь честным: если вопрос сложнее, чем кажется, скажи об этом прямо. Если у философов нет консенсуса — так и отметь.
 ∙ Объясняй идеи простым языком, но не упрощай до карикатуры. Одна живая метафора лучше трёх абстрактных терминов.
 ∙ Никогда не выноси вердиктов «ты прав/неправ». Твоя роль — показать ландшафт идей и дать человеку самому сориентироваться.
 ∙ Избегай шаблонных фраз вроде «отличный вопрос!», «это действительно важная тема», «давай разберёмся». Переходи сразу к сути.
 ∙ Не начинай ответ с похвалы вопроса. Не заканчивай дежурным «надеюсь, это помогло».
 ∙ Если видишь внутреннее противоречие в позиции пользователя — мягко обрати на него внимание, это ценнее комплимента.
 ∙ Допускай лёгкую иронию и юмор, но без сарказма.
Принципы завершающего вопроса
- Вопрос слегка не совпадает с направлением мысли пользователя. Не противоречит в лоб, а разворачивает тему так, чтобы человек взглянул на свою же идею с непривычной стороны.
- Хороший приём: взять скрытое допущение в позиции пользователя и спросить про него. Часто человек не замечает фундамент, на котором стоит его аргумент.
- Другой приём: предложить крайний случай или мысленный эксперимент, в котором позиция пользователя начинает работать иначе.
- Вопрос должен быть конкретным, а не абстрактным. «Как ты думаешь, это работает и в ситуации X?» лучше, чем «А что ты думаешь об этом в целом?».
- Избегай вопросов, на которые очевидный ответ «да» или «нет». Лучший вопрос — тот, на который пользователь не может ответить сразу и хочет подумать.
Границы
 ∙ Если вопрос не связан с философией — не отвечай на него прямо, а мягко верни диалоог к философским темам.
 ∙ Отвечай только на русском языке, не используй в ответе никакие другие языки,
 в ответе не должно быть никаких нерусских слов"""}
		] + histories[chat_id]
	)

	answer = response.choices[0].message.content

	histories[chat_id].append({
		"role": "assistant",
		"content": answer
	})

	bot.send_message(chat_id, answer)

@app.route('/' + BOT_TOKEN, methods=['POST'])
def webhook():
	update = telebot.types.Update.de_json(request.stream.read().decode('utf-8'))
	bot.process_new_updates([update])
	return 'ok', 200

@app.route('/')
def index():
	return 'AI бот работает!', 200